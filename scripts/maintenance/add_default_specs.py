"""One-shot helper: inject ``SPEC = make_default_spec(...)`` into report modules.

Walks ``scripts/report_generators/reports/*.py`` and adds a top-level
``SPEC`` constant to any class-based report that doesn't have one yet.
The generated SPEC is minimally-valid (passes ``ReportSpec.validate()``)
and uses audience-default forbidden_framing.

Each report's ``audience``, ``name``, and ``meta_table`` are inferred
from existing fields; ``claim`` is derived from ``title`` + ``subtitle``.
Reports already exporting ``SPEC`` are skipped.

Run from project root:
    python scripts/maintenance/add_default_specs.py
"""

from __future__ import annotations

import ast
import sys
from pathlib import Path

REPORTS_DIR = Path(__file__).resolve().parents[1] / "report_generators" / "reports"

# Map class-name suffix → audience.
_AUDIENCE_BY_PREFIX: list[tuple[str, str]] = [
    ("policy_", "policy"),
    ("hr_", "hr"),
    ("mgmt_", "hr"),
    ("growth_", "hr"),
    ("biz_", "biz"),
    ("o1_", "policy"),
    ("o2_", "hr"),
    ("o3_", "biz"),
    ("o4_", "policy"),
    ("o7_", "technical_appendix"),
    ("o8_", "biz"),
    ("akm_", "technical_appendix"),
    ("dml_", "technical_appendix"),
    ("score_", "technical_appendix"),
    ("shap_", "technical_appendix"),
    ("longitudinal", "technical_appendix"),
    ("ml_", "technical_appendix"),
    ("network_", "technical_appendix"),
    ("cooccurrence", "technical_appendix"),
    ("madb_", "technical_appendix"),
    ("derived_", "technical_appendix"),
    ("cohort_", "technical_appendix"),
    ("knowledge_", "technical_appendix"),
    ("temporal_", "technical_appendix"),
    ("bridge_", "technical_appendix"),
    ("bias_", "common"),
    ("compensation_", "policy"),
    ("industry_", "common"),
    ("person_", "common"),
    ("index_", "common"),
    ("career_", "policy"),
    ("genre_", "biz"),
    ("studio_", "hr"),
    ("team_", "hr"),
    ("structural_", "hr"),
    ("compatibility", "hr"),
    ("anime_value", "common"),
    ("expected_", "common"),
    ("exit_", "policy"),
    ("industry_analysis", "common"),
]

# Skip these (no class-based report or brief index)
SKIP_FILES = {"__init__.py", "_base.py"}


def infer_audience(stem: str) -> str:
    for prefix, aud in _AUDIENCE_BY_PREFIX:
        if stem.startswith(prefix):
            return aud
    if "brief_index" in stem:
        return "common"
    return "common"


def parse_report_class(path: Path) -> dict | None:
    """Extract class-level fields (name, title, filename, etc.) by AST."""
    src = path.read_text(encoding="utf-8")
    try:
        tree = ast.parse(src)
    except SyntaxError:
        return None
    for node in ast.iter_child_nodes(tree):
        if not isinstance(node, ast.ClassDef):
            continue
        # Must inherit from BaseReportGenerator
        bases = [b.id if isinstance(b, ast.Name) else "" for b in node.bases]
        if "BaseReportGenerator" not in bases:
            continue
        info: dict[str, str] = {"class_name": node.name}
        for stmt in node.body:
            if isinstance(stmt, ast.Assign):
                for target in stmt.targets:
                    if isinstance(target, ast.Name) and isinstance(
                        stmt.value, ast.Constant
                    ):
                        info[target.id] = stmt.value.value
        return info
    return None


def has_top_level_spec(path: Path) -> bool:
    src = path.read_text(encoding="utf-8")
    try:
        tree = ast.parse(src)
    except SyntaxError:
        return False
    for node in ast.iter_child_nodes(tree):
        if isinstance(node, ast.Assign):
            for target in node.targets:
                if isinstance(target, ast.Name) and target.id == "SPEC":
                    return True
    return False


def render_spec_block(info: dict, audience: str) -> str:
    name = info.get("name", "")
    title = info.get("title", name)
    subtitle = info.get("subtitle", "")
    claim = (
        f"{title} に関する記述的指標 (subtitle: {subtitle})"
        if subtitle
        else f"{title} に関する記述的指標"
    )
    meta_table = f"meta_{name}"
    return f'''

# v3 minimal SPEC — generated by scripts/maintenance/add_default_specs.py.
# Replace ``claim`` / ``identifying_assumption`` / ``null_model`` with
# report-specific values when curating this module.
from .._spec import make_default_spec  # noqa: E402

SPEC = make_default_spec(
    name={name!r},
    audience={audience!r},
    claim={claim!r},
    sources=["credits", "persons", "anime"],
    meta_table={meta_table!r},
)
'''


def main() -> int:
    n_added = 0
    n_skipped = 0
    for path in sorted(REPORTS_DIR.glob("*.py")):
        if path.name in SKIP_FILES or path.name.startswith("_"):
            n_skipped += 1
            continue
        if has_top_level_spec(path):
            print(f"  [skip] {path.name}: already has SPEC")
            n_skipped += 1
            continue
        info = parse_report_class(path)
        if not info or not info.get("name"):
            print(f"  [skip] {path.name}: no BaseReportGenerator subclass")
            n_skipped += 1
            continue
        audience = infer_audience(path.stem)
        block = render_spec_block(info, audience)
        with path.open("a", encoding="utf-8") as f:
            f.write(block)
        print(f"  [add ] {path.name}: SPEC ({audience})")
        n_added += 1
    print(f"\nAdded SPEC to {n_added} files, skipped {n_skipped}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
