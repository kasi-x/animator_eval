"""HTML export for credit statistics with D3.js visualizations.

Generates interactive HTML reports with D3.js charts for credit-based analysis.
Includes role distribution, collaboration networks, timeline analysis, and person statistics.
"""

import json
from pathlib import Path
from typing import Any

import structlog

logger = structlog.get_logger()


def generate_credit_stats_html(
    credit_stats: dict[str, Any],
    output_path: Path | str,
    title: str = "Credit Statistics Report (クレジット統計レポート)",
) -> None:
    """Generate interactive HTML report with D3.js visualizations.

    Creates an HTML page with:
    - Role distribution bar chart (horizontal)
    - Timeline line chart (credits per year)
    - Collaboration network statistics
    - Person-level statistics histograms
    - Interactive tooltips and legends

    Args:
        credit_stats: Credit statistics data from compute_credit_statistics()
        output_path: Path to save HTML file
        title: Report title

    Example:
        >>> from src.analysis.credit_stats import compute_credit_statistics
        >>> stats = compute_credit_statistics(credits, anime_map)
        >>> generate_credit_stats_html(stats, "result/html/credit_stats.html")
    """
    if not credit_stats:
        logger.warning("empty_credit_stats_for_html")
        return

    # Build HTML content
    html_content = _build_html_structure(credit_stats, title)

    # Write to file
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(html_content, encoding="utf-8")

    logger.info("credit_stats_html_saved", path=str(output_path))


def _build_html_structure(credit_stats: dict[str, Any], title: str) -> str:
    """Build complete HTML document with D3.js visualizations."""
    # Serialize data to JSON for JavaScript
    data_json = json.dumps(credit_stats, ensure_ascii=False, indent=2)

    html = f"""<!DOCTYPE html>
<html lang="ja">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>{title}</title>
    <script src="https://d3js.org/d3.v7.min.js"></script>
    <style>
        {_get_css_styles()}
    </style>
</head>
<body>
    <div class="container">
        <header>
            <h1>{title}</h1>
            <p class="subtitle">Person ID Level Analysis — クレジットデータ直接分析</p>
        </header>

        <section class="summary-section">
            <h2>📊 Summary Statistics (サマリー統計)</h2>
            <div class="summary-grid">
                <div class="summary-card">
                    <div class="summary-value">{credit_stats.get("summary", {}).get("total_credits", 0):,}</div>
                    <div class="summary-label">Total Credits</div>
                </div>
                <div class="summary-card">
                    <div class="summary-value">{credit_stats.get("summary", {}).get("unique_persons", 0):,}</div>
                    <div class="summary-label">Unique Persons</div>
                </div>
                <div class="summary-card">
                    <div class="summary-value">{credit_stats.get("summary", {}).get("unique_anime", 0):,}</div>
                    <div class="summary-label">Unique Anime</div>
                </div>
                <div class="summary-card">
                    <div class="summary-value">{credit_stats.get("summary", {}).get("avg_credits_per_person", 0):.1f}</div>
                    <div class="summary-label">Avg Credits/Person</div>
                </div>
            </div>
        </section>

        <section class="chart-section">
            <h2>📈 Role Distribution (ロール分布 Top 20)</h2>
            <div id="role-chart"></div>
        </section>

        <section class="chart-section">
            <h2>📅 Timeline Analysis (時系列分析)</h2>
            <div id="timeline-chart"></div>
        </section>

        <section class="chart-section">
            <h2>🤝 Collaboration Statistics (協力関係統計)</h2>
            <div class="collab-stats">
                <p><strong>Total Pair Instances:</strong> {credit_stats.get("collaboration_stats", {}).get("total_pair_instances", credit_stats.get("collaboration_stats", {}).get("total_pairs", 0)):,}</p>
            </div>
            <div id="collab-chart"></div>
        </section>

        <section class="chart-section">
            <h2>👤 Person Statistics (Person統計)</h2>
            <div id="person-stats-chart"></div>
        </section>

        <footer>
            <p>Generated by Animetor Eval - Credit Statistics Module</p>
            <p>Powered by D3.js v7</p>
        </footer>
    </div>

    <script>
        // Data
        const creditStats = {data_json};

        // Chart 1: Role Distribution (Horizontal Bar Chart)
        function drawRoleChart() {{
            const data = creditStats.top_roles.slice(0, 20);

            const margin = {{top: 20, right: 60, bottom: 40, left: 150}};
            const width = 900 - margin.left - margin.right;
            const height = 600 - margin.top - margin.bottom;

            const svg = d3.select("#role-chart")
                .append("svg")
                .attr("width", width + margin.left + margin.right)
                .attr("height", height + margin.top + margin.bottom)
                .append("g")
                .attr("transform", `translate(${{margin.left}},${{margin.top}})`);

            const x = d3.scaleLinear()
                .domain([0, d3.max(data, d => d.count)])
                .range([0, width]);

            const y = d3.scaleBand()
                .domain(data.map(d => d.role))
                .range([0, height])
                .padding(0.2);

            // Bars
            svg.selectAll(".bar")
                .data(data)
                .enter()
                .append("rect")
                .attr("class", "bar")
                .attr("x", 0)
                .attr("y", d => y(d.role))
                .attr("width", d => x(d.count))
                .attr("height", y.bandwidth())
                .attr("fill", "#4CAF50")
                .on("mouseover", function(event, d) {{
                    d3.select(this).attr("fill", "#45a049");
                    tooltip.style("opacity", 1)
                        .html(`<strong>${{d.role}}</strong><br/>Count: ${{d.count.toLocaleString()}}<br/>Percentage: ${{d.percentage}}%`)
                        .style("left", (event.pageX + 10) + "px")
                        .style("top", (event.pageY - 28) + "px");
                }})
                .on("mouseout", function() {{
                    d3.select(this).attr("fill", "#4CAF50");
                    tooltip.style("opacity", 0);
                }});

            // Count labels
            svg.selectAll(".count-label")
                .data(data)
                .enter()
                .append("text")
                .attr("class", "count-label")
                .attr("x", d => x(d.count) + 5)
                .attr("y", d => y(d.role) + y.bandwidth() / 2)
                .attr("dy", "0.35em")
                .text(d => d.count.toLocaleString())
                .style("font-size", "11px")
                .style("fill", "#333");

            // Axes
            svg.append("g")
                .attr("class", "y-axis")
                .call(d3.axisLeft(y));

            svg.append("g")
                .attr("class", "x-axis")
                .attr("transform", `translate(0,${{height}})`)
                .call(d3.axisBottom(x).ticks(5).tickFormat(d3.format(".0s")));

            // Tooltip
            const tooltip = d3.select("body").append("div")
                .attr("class", "tooltip")
                .style("opacity", 0);
        }}

        // Chart 2: Timeline (Line Chart)
        function drawTimelineChart() {{
            const data = creditStats.timeline_stats.by_year
                .filter(d => d.year >= 1990)  // Focus on 1990+
                .sort((a, b) => a.year - b.year);

            const margin = {{top: 20, right: 120, bottom: 40, left: 60}};
            const width = 900 - margin.left - margin.right;
            const height = 400 - margin.top - margin.bottom;

            const svg = d3.select("#timeline-chart")
                .append("svg")
                .attr("width", width + margin.left + margin.right)
                .attr("height", height + margin.top + margin.bottom)
                .append("g")
                .attr("transform", `translate(${{margin.left}},${{margin.top}})`);

            const x = d3.scaleLinear()
                .domain(d3.extent(data, d => d.year))
                .range([0, width]);

            const y = d3.scaleLinear()
                .domain([0, d3.max(data, d => d.credits)])
                .range([height, 0]);

            // Line
            const line = d3.line()
                .x(d => x(d.year))
                .y(d => y(d.credits))
                .curve(d3.curveMonotoneX);

            svg.append("path")
                .datum(data)
                .attr("class", "line")
                .attr("fill", "none")
                .attr("stroke", "#2196F3")
                .attr("stroke-width", 2)
                .attr("d", line);

            // Dots
            svg.selectAll(".dot")
                .data(data)
                .enter()
                .append("circle")
                .attr("class", "dot")
                .attr("cx", d => x(d.year))
                .attr("cy", d => y(d.credits))
                .attr("r", 4)
                .attr("fill", "#2196F3")
                .on("mouseover", function(event, d) {{
                    d3.select(this).attr("r", 6);
                    tooltip.style("opacity", 1)
                        .html(`<strong>${{d.year}}</strong><br/>Credits: ${{d.credits.toLocaleString()}}<br/>Anime: ${{d.anime_count}}<br/>Persons: ${{d.person_count}}`)
                        .style("left", (event.pageX + 10) + "px")
                        .style("top", (event.pageY - 28) + "px");
                }})
                .on("mouseout", function() {{
                    d3.select(this).attr("r", 4);
                    tooltip.style("opacity", 0);
                }});

            // Axes
            svg.append("g")
                .attr("class", "x-axis")
                .attr("transform", `translate(0,${{height}})`)
                .call(d3.axisBottom(x).tickFormat(d3.format("d")));

            svg.append("g")
                .attr("class", "y-axis")
                .call(d3.axisLeft(y).tickFormat(d3.format(".0s")));

            // Labels
            svg.append("text")
                .attr("x", width / 2)
                .attr("y", height + 35)
                .attr("text-anchor", "middle")
                .text("Year");

            svg.append("text")
                .attr("transform", "rotate(-90)")
                .attr("x", -height / 2)
                .attr("y", -45)
                .attr("text-anchor", "middle")
                .text("Credits");

            // Tooltip
            const tooltip = d3.select("body").append("div")
                .attr("class", "tooltip")
                .style("opacity", 0);
        }}

        // Chart 3: Top Collaborations (Bubble Chart)
        function drawCollabChart() {{
            const data = (creditStats.collaboration_stats.top_collaborations || []).slice(0, 30);
            if (data.length === 0) {{ d3.select("#collab-chart").append("p").text("Collaboration pair data deferred to collaboration_strength module."); return; }}

            const margin = {{top: 20, right: 20, bottom: 80, left: 60}};
            const width = 900 - margin.left - margin.right;
            const height = 400 - margin.top - margin.bottom;

            const svg = d3.select("#collab-chart")
                .append("svg")
                .attr("width", width + margin.left + margin.right)
                .attr("height", height + margin.top + margin.bottom)
                .append("g")
                .attr("transform", `translate(${{margin.left}},${{margin.top}})`);

            const x = d3.scaleLinear()
                .domain([0, data.length])
                .range([0, width]);

            const y = d3.scaleLinear()
                .domain([0, d3.max(data, d => d.shared_anime_count)])
                .range([height, 0]);

            const r = d3.scaleSqrt()
                .domain([0, d3.max(data, d => d.shared_anime_count)])
                .range([3, 15]);

            // Bubbles
            svg.selectAll(".bubble")
                .data(data)
                .enter()
                .append("circle")
                .attr("class", "bubble")
                .attr("cx", (d, i) => x(i))
                .attr("cy", d => y(d.shared_anime_count))
                .attr("r", d => r(d.shared_anime_count))
                .attr("fill", "#FF9800")
                .attr("opacity", 0.7)
                .on("mouseover", function(event, d) {{
                    d3.select(this).attr("opacity", 1);
                    tooltip.style("opacity", 1)
                        .html(`<strong>Pair #${{data.indexOf(d) + 1}}</strong><br/>Shared Anime: ${{d.shared_anime_count}}<br/>Person 1: ${{d.person_id_1}}<br/>Person 2: ${{d.person_id_2}}`)
                        .style("left", (event.pageX + 10) + "px")
                        .style("top", (event.pageY - 28) + "px");
                }})
                .on("mouseout", function() {{
                    d3.select(this).attr("opacity", 0.7);
                    tooltip.style("opacity", 0);
                }});

            // Axes
            svg.append("g")
                .attr("class", "x-axis")
                .attr("transform", `translate(0,${{height}})`)
                .call(d3.axisBottom(x).ticks(10));

            svg.append("g")
                .attr("class", "y-axis")
                .call(d3.axisLeft(y));

            // Labels
            svg.append("text")
                .attr("x", width / 2)
                .attr("y", height + 35)
                .attr("text-anchor", "middle")
                .text("Collaboration Pair Rank");

            svg.append("text")
                .attr("transform", "rotate(-90)")
                .attr("x", -height / 2)
                .attr("y", -45)
                .attr("text-anchor", "middle")
                .text("Shared Anime Count");

            // Tooltip
            const tooltip = d3.select("body").append("div")
                .attr("class", "tooltip")
                .style("opacity", 0);
        }}

        // Chart 4: Person Statistics (Histogram)
        function drawPersonStatsChart() {{
            const topPersons = creditStats.person_id_stats.top_persons_by_credits.slice(0, 50);

            const margin = {{top: 20, right: 20, bottom: 80, left: 60}};
            const width = 900 - margin.left - margin.right;
            const height = 400 - margin.top - margin.bottom;

            const svg = d3.select("#person-stats-chart")
                .append("svg")
                .attr("width", width + margin.left + margin.right)
                .attr("height", height + margin.top + margin.bottom)
                .append("g")
                .attr("transform", `translate(${{margin.left}},${{margin.top}})`);

            const x = d3.scaleBand()
                .domain(topPersons.map((d, i) => i + 1))
                .range([0, width])
                .padding(0.1);

            const y = d3.scaleLinear()
                .domain([0, d3.max(topPersons, d => d.credit_count)])
                .range([height, 0]);

            // Bars
            svg.selectAll(".bar")
                .data(topPersons)
                .enter()
                .append("rect")
                .attr("class", "bar")
                .attr("x", (d, i) => x(i + 1))
                .attr("y", d => y(d.credit_count))
                .attr("width", x.bandwidth())
                .attr("height", d => height - y(d.credit_count))
                .attr("fill", "#9C27B0")
                .on("mouseover", function(event, d) {{
                    d3.select(this).attr("fill", "#7B1FA2");
                    tooltip.style("opacity", 1)
                        .html(`<strong>Rank #${{topPersons.indexOf(d) + 1}}</strong><br/>Person ID: ${{d.person_id}}<br/>Credits: ${{d.credit_count}}`)
                        .style("left", (event.pageX + 10) + "px")
                        .style("top", (event.pageY - 28) + "px");
                }})
                .on("mouseout", function() {{
                    d3.select(this).attr("fill", "#9C27B0");
                    tooltip.style("opacity", 0);
                }});

            // Axes
            svg.append("g")
                .attr("class", "x-axis")
                .attr("transform", `translate(0,${{height}})`)
                .call(d3.axisBottom(x).tickValues(x.domain().filter((d, i) => i % 5 === 0)));

            svg.append("g")
                .attr("class", "y-axis")
                .call(d3.axisLeft(y));

            // Labels
            svg.append("text")
                .attr("x", width / 2)
                .attr("y", height + 35)
                .attr("text-anchor", "middle")
                .text("Person Rank (by Credit Count)");

            svg.append("text")
                .attr("transform", "rotate(-90)")
                .attr("x", -height / 2)
                .attr("y", -45)
                .attr("text-anchor", "middle")
                .text("Credit Count");

            // Tooltip
            const tooltip = d3.select("body").append("div")
                .attr("class", "tooltip")
                .style("opacity", 0);
        }}

        // Initialize all charts
        drawRoleChart();
        drawTimelineChart();
        drawCollabChart();
        drawPersonStatsChart();
    </script>
</body>
</html>
"""
    return html


