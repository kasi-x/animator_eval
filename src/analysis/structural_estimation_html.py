def _get_timestamp() -> str:
    """Get current timestamp in Japanese format."""
    from datetime import datetime
    return datetime.now().strftime("%Y年%m月%d日 %H:%M:%S")


def _build_executive_summary(result: Any) -> str:
    """Build executive summary section."""
    fe = result.fixed_effects
    did = result.did_estimate
    preferred = result.preferred_estimate

    # Determine significance
    fe_sig = "有意" if fe.p_value < 0.05 else "非有意"
    did_sig = "有意" if did.p_value < 0.05 else "非有意"

    # Event study status
    event_study_available = result.event_study is not None
    parallel_trends_result = result.parallel_trends_test.get("result", "not_tested") if result.parallel_trends_test else "not_tested"

    html = f"""
    <section class="section">
        <h2>📊 要約 (Executive Summary)</h2>

        <div class="summary-grid">
            <div class="summary-card">
                <h4>固定効果推定 (FE)</h4>
                <div class="value">{fe.beta:.2f}</div>
                <div class="label">点推定値 (p={fe.p_value:.3f})</div>
                <span class="badge badge-{'significant' if fe.p_value < 0.05 else 'not-significant'}">{fe_sig}</span>
            </div>

            <div class="summary-card">
                <h4>差分の差分法 (DID)</h4>
                <div class="value">{did.beta:.2f}</div>
                <div class="label">点推定値 (p={did.p_value:.3f})</div>
                <span class="badge badge-{'significant' if did.p_value < 0.05 else 'not-significant'}">{did_sig}</span>
            </div>

            <div class="summary-card">
                <h4>推奨推定値</h4>
                <div class="value">{preferred.beta:.2f}</div>
                <div class="label">{preferred.method.value}</div>
                <span class="badge badge-significant">採用</span>
            </div>

            <div class="summary-card">
                <h4>並行トレンド検定</h4>
                <div class="value">{'✓' if parallel_trends_result == 'passed' else '✗' if parallel_trends_result == 'failed' else '?'}</div>
                <div class="label">{'成立' if parallel_trends_result == 'passed' else '不成立' if parallel_trends_result == 'failed' else '未実施' if parallel_trends_result == 'not_tested' else '警告'}</div>
                <span class="badge badge-{parallel_trends_result}">{parallel_trends_result.upper()}</span>
            </div>
        </div>

        <div class="interpretation">
            <h4>結論</h4>
            <p>{preferred.interpretation}</p>
        </div>
    </section>
    """
    return html


def _build_event_study_section(result: Any) -> str:
    """Build event study section with coefficient table."""
    if not result.event_study:
        return """
        <section class="section">
            <h2>📈 Event Study (イベントスタディ)</h2>
            <div class="alert alert-warning">
                Event Studyの結果が利用できません。サンプルサイズが不足している可能性があります。
            </div>
        </section>
        """

    event_study = result.event_study
    parallel_trends = result.parallel_trends_test

    # Build coefficient table
    table_rows = []
    for k in sorted(event_study.keys()):
        coef = event_study[k]
        period_type = "処置前" if k < 0 else "入所年" if k == 0 else "処置後"
        row_class = "coefficient-row-pre" if k < 0 else "coefficient-row-treatment" if k == 0 else "coefficient-row-post"

        sig_badge = f'<span class="badge badge-significant">有意</span>' if coef.p_value < 0.05 else f'<span class="badge badge-not-significant">非有意</span>'

        table_rows.append(f"""
            <tr class="{row_class}">
                <td>k = {k:+d}</td>
                <td>{period_type}</td>
                <td>{coef.beta:.3f}</td>
                <td>{coef.se:.3f}</td>
                <td>[{coef.ci_lower:.2f}, {coef.ci_upper:.2f}]</td>
                <td>{coef.p_value:.4f}</td>
                <td>{sig_badge}</td>
            </tr>
        """)

    table_html = f"""
        <table>
            <thead>
                <tr>
                    <th>相対時点</th>
                    <th>期間</th>
                    <th>係数 (β_k)</th>
                    <th>標準誤差</th>
                    <th>95% CI</th>
                    <th>p値</th>
                    <th>有意性</th>
                </tr>
            </thead>
            <tbody>
                {''.join(table_rows)}
            </tbody>
        </table>
    """

    # Parallel trends test result
    if parallel_trends:
        pt_result = parallel_trends.get("result", "inconclusive")
        pt_detail = parallel_trends.get("detail", "検定結果なし")
        pt_evidence = parallel_trends.get("evidence", {})

        pt_badge_class = f"badge-{pt_result}"
        pt_alert_class = "alert-success" if pt_result == "passed" else "alert-danger" if pt_result == "failed" else "alert-warning"

        parallel_trends_html = f"""
            <div class="alert {pt_alert_class}">
                <strong>並行トレンド検定:</strong> <span class="badge {pt_badge_class}">{pt_result.upper()}</span>
                <p style="margin-top: 0.5rem;">{pt_detail}</p>
                {f'<p style="margin-top: 0.3rem;">平均 |β_pre| = {pt_evidence.get("avg_abs_beta", 0):.2f}, 最大 |β_pre| = {pt_evidence.get("max_abs_beta", 0):.2f}</p>' if pt_evidence else ''}
            </div>
        """
    else:
        parallel_trends_html = ""

    html = f"""
    <section class="section">
        <h2>📈 Event Study (イベントスタディ)</h2>

        <p>大手スタジオ入所前後の動的処置効果を推定しました。処置前係数（k < 0）がゼロ付近であれば、並行トレンド仮定が成立していることを示します。</p>

        {parallel_trends_html}

        <h3>係数推定値</h3>
        {table_html}

        <div class="interpretation">
            <h4>解釈</h4>
            <ul>
                <li><strong>処置前（k < 0）</strong>: これらの係数がゼロ付近にあれば、並行トレンド仮定が成立。処置前から既に差があれば、選択バイアスの可能性。</li>
                <li><strong>入所年（k = 0）</strong>: 大手スタジオ入所の即時効果。有意にプラスであれば、処置効果の証拠。</li>
                <li><strong>処置後（k > 0）</strong>: 入所後の累積効果。時間とともに増加すれば、学習効果・教育効果の証拠。</li>
            </ul>
        </div>
    </section>
    """
    return html


