"""HTML export for structural estimation results.

Generates interactive HTML reports with tables, plots, and interpretations
for publication-quality presentation of causal inference results.
"""

from pathlib import Path
from typing import Any

import structlog

logger = structlog.get_logger()


def generate_html_report(
    result: Any,  # StructuralEstimationResult
    output_path: Path | str,
    title: str = "Structural Estimation Report (構造推定レポート)",
) -> None:
    """Generate comprehensive HTML report for structural estimation.

    Creates an interactive HTML page with:
    - Executive summary
    - Event study plot (embedded as base64 image)
    - Coefficient tables
    - Parallel trends test results
    - Fixed effects and DID estimates
    - Robustness checks
    - Interpretation and recommendations

    Args:
        result: StructuralEstimationResult object
        output_path: Path to save HTML file
        title: Report title

    Example:
        >>> from src.analysis.structural_estimation import estimate_structural_model
        >>> result = estimate_structural_model(...)
        >>> generate_html_report(result, "report.html")
    """
    # Build HTML content
    html_content = _build_html_structure(result, title)

    # Write to file
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(html_content, encoding='utf-8')

    logger.info("structural_estimation_html_saved", path=str(output_path))


def _build_html_structure(result: Any, title: str) -> str:
    """Build complete HTML document structure."""
    html = f"""<!DOCTYPE html>
<html lang="ja">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>{title}</title>
    <style>
        {_get_css_styles()}
    </style>
</head>
<body>
    <div class="container">
        <header>
            <h1>{title}</h1>
            <p class="subtitle">大手スタジオ効果の因果推論分析</p>
            <p class="timestamp">生成日時: {_get_timestamp()}</p>
        </header>

        {_build_executive_summary(result)}
        {_build_event_study_section(result)}
        {_build_main_estimates_section(result)}
        {_build_robustness_section(result)}
        {_build_interpretation_section(result)}

        <footer>
            <p>Generated by Animetor Eval - Structural Estimation Module</p>
            <p>© 2026 Claude Opus 4.6</p>
        </footer>
    </div>
</body>
</html>
"""
    return html


